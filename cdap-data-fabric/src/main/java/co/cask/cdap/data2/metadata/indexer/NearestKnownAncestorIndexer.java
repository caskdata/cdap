package co.cask.cdap.data2.metadata.indexer;

import co.cask.cdap.api.metadata.MetadataEntity;
import co.cask.cdap.data2.metadata.dataset.MetadataDataset;
import co.cask.cdap.data2.metadata.dataset.MetadataEntry;
import co.cask.cdap.data2.metadata.dataset.SortInfo;
import co.cask.cdap.proto.element.EntityType;
import co.cask.cdap.proto.id.EntityId;

import java.util.HashSet;
import java.util.Set;

/**
 * Indexer used to index a custom {@link MetadataEntity} with it's nearest known ancestor.
 * A nearest known ancestor is defined as the nearest known {@link EntityType} in the hierarchy which is determined by
 * {@link EntityId#getNearestKnownEntity(MetadataEntity)}. If the given {@link MetadataEntity} is not a custom
 * entity and is a know CDAP entity then no indexes are generated by this indexer.
 */
public class NearestKnownAncestorIndexer implements Indexer {

  static final String NEAREST_KEY = "nearest_known_entity";

  @Override
  public Set<String> getIndexes(MetadataEntry entry) {
    Set<String> indexes = new HashSet<>();
    if (entry.getMetadataEntity().isCustomEntity()) {
      // we want to generate the index only for custom metadata entity. For known entity we don't index them with their
      // last known ancestor entity
      indexes.add(getSelfOrNearestKnownAncestorIndex(entry.getMetadataEntity()));
    }
    return indexes;
  }

  /**
   * Returns the nearest known entity index. The nearest known entity is determines by
   * {@link EntityId#getNearestKnownEntity(MetadataEntity)}.
   *
   * @param metadataEntity the {@link MetadataEntity} for which the index needs to be generated
   * @return the index
   */
  public String getSelfOrNearestKnownAncestorIndex(MetadataEntity metadataEntity) {
    EntityId nearestKnownEntity = EntityId.getNearestKnownEntity(metadataEntity);
    return NEAREST_KEY + MetadataDataset.KEYVALUE_SEPARATOR + nearestKnownEntity;
  }

  @Override
  public SortInfo.SortOrder getSortOrder() {
    return SortInfo.SortOrder.WEIGHTED;
  }
}
